 */
public function findHistoriqueAdherent(Adherent $adherent): array
{
    return $this->createQueryBuilder('e')
        ->where('e.adherent = :adherent')
        ->setParameter('adherent', $adherent)
        ->orderBy('e.dateEmprunt', 'DESC')
        ->getQuery()
        ->getResult();
}
